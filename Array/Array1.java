package Array;

public class Array1 {
	public static void main(String [] args) {
		int x[];
		x = new int[5];
		System.out.println("Size of Array "+x.length);
		int x1[] = new int [12];
		System.out.println("Size of Array "+x1.length);		
		short x2[] = new short [214748399]; 
		System.out.println("Size of Array "+x2.length);	
	}
}
